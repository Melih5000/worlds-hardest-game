	public void grapCaller(Graphics g){
		this.drawWalls(g);
		for(Enemy e : enemies){
			e.drawEnemy(g);
		}
		for(Coin c : coins){
			if(!c.isCollected())
				c.drawCoin(g);
		}


	}

	public void levelMove(int a, int b){
		this.e1.enemyVerticalMove(a,b);
		this.e2.enemyVerticalMove(a,b);
		this.e3.enemyHorizontalMove(96,144);
	}

	public void drawWalls(Graphics g){

		for(int i=0 ; i < walls.length ; i++){
			for(int j=0; j < walls[i].length ; j++){
				if(walls[i][j] == 1){
					Rectangle r = new Rectangle(j*16,i*16,16,16);
					g.setColor(new Color(0x00000000, true));
					g.fillRect(j*16,i*16,16,16);
					wallRecs.add(r);
				}
			}
		}
	}

	public void enemyUpdate(){
		for(Enemy e : enemies){
			e.update();
		}
	}

